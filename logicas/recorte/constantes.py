# Constantes para el sistema de recorte de cartas VTES

# Aspect ratio estándar de cartas de trading cards (VTES, MTG, etc.)
# Tamaño estándar: 63mm x 88mm
VTES_CARD_ASPECT_RATIO = 63 / 88  # ≈ 0.7159

# Dimensiones estándar en píxeles para alta resolución (300 DPI)
# 63mm = 2.48 pulgadas, 88mm = 3.46 pulgadas
# A 300 DPI: 744 x 1038 píxeles
VTES_CARD_WIDTH_300DPI = 744
VTES_CARD_HEIGHT_300DPI = 1038

# Dimensiones optimizadas para juego online (menor tamaño para mejor visualización)
VTES_CARD_WIDTH_ONLINE = 358
VTES_CARD_HEIGHT_ONLINE = 500
